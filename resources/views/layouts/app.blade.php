    <script type="module">
        setTimeout( ()=> {
            Echo.channel('completedTasksChannel')
            .listen('CompletedTasks',(e)=>{
                console.log(e)
                if ('completed_tasks' in e){
                    e['completed_tasks'].forEach(function(task , key){
                        Toastify({
                            text: task.title + " completed",
                            duration: 3000,
                            style: {
                                background: "linear-gradient(to right, #00b09b, #96c93d)",
                            },
                        }).showToast();
                    })
                }
            })
        })

        setTimeout( ()=> {
            Echo.channel('uncompletedTasksChannel')
                .listen('UncompletedTasks',(e)=>{
                    console.log(e)
                    if ('uncompleted_tasks' in e){
                        e['uncompleted_tasks'].forEach(function(task , key){
                            Toastify({
                                text: task.title + " uncompleted",
                                duration: 3000,
                                style: {
                                    background: "linear-gradient(to right, #bb0909, #960000)",
                                },
                            }).showToast();
                        })
                    }
                })
        })
    </script>
